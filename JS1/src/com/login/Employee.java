package com.login;

public class Employee {
	
	String EmpName;
	String EmpPass;
	
	public String getEmpName() {
		return EmpName;
	}
	public void setEmpName(String empName) {
		EmpName = empName;
	}
	public String getEmpPass() {
		return EmpPass;
	}
	public void setEmpPass(String empPass) {
		EmpPass = empPass;
	}
	

}
